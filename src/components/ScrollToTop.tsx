/**
 * ScrollToTop — плавающая кнопка «Вернуться наверх».
 *
 * Появляется в правом нижнем углу, когда пользователь прокручивает страницу
 * более чем на SCROLL_THRESHOLD пикселей. По клику плавно скроллит к началу.
 *
 * ── Позиционирование ─────────────────────────────────────────────────────────
 *
 *   Все экраны: bottom-6 right-6 (24px от низа и правого края).
 *
 *   Конкурирующих fixed-элементов нет: на /admin кнопка "+" теперь постоянно
 *   находится в шапке страницы (не FAB), поэтому специального смещения
 *   для мобильных не требуется.
 *
 * ── Стек z-index ─────────────────────────────────────────────────────────────
 *
 *   z-50 — поверх контента страницы, но ниже модальных окон (z-[9999]).
 *
 * ── Производительность ───────────────────────────────────────────────────────
 *
 *   Слушатель скролла добавлен с { passive: true } — браузер знает, что
 *   preventDefault() вызван не будет, и может оптимизировать обработку скролла.
 *   Это устраняет предупреждение Lighthouse про non-passive event listeners.
 *
 * ── Доступность (a11y) ───────────────────────────────────────────────────────
 *
 *   aria-label="Вернуться наверх" — для screen readers.
 *   Кнопка рендерится только когда видима (return null) — не занимает место
 *   в DOM и не мешает навигации по Tab когда прокрутки нет.
 *
 * ── Анимация появления ───────────────────────────────────────────────────────
 *
 *   Использует animate-fade-in из globals.css (opacity 0→1, translateY 4px→0).
 *   Поскольку компонент условно монтируется (null → button), анимация
 *   запускается каждый раз при появлении кнопки.
 */
'use client';

import { useEffect, useState } from 'react';

/**
 * Порог прокрутки в пикселях, после которого кнопка становится видимой.
 *
 * Значение 400px выбрано из расчёта: примерно 1–1.5 экрана вниз.
 * Показывать кнопку сразу (0px) — бессмысленно, слишком рано.
 * 400px — пользователь явно начал читать/листать и может захотеть вернуться.
 */
const SCROLL_THRESHOLD = 400;

export function ScrollToTop() {
    /** Управляет видимостью кнопки на основе текущей позиции скролла */
    const [visible, setVisible] = useState(false);

    useEffect(() => {
        /**
         * Обработчик события скролла.
         * Простая булева проверка — не нужен дополнительный debounce,
         * т.к. изменение state в React батчируется и не вызывает
         * лишних ре-рендеров при каждом событии.
         */
        const handleScroll = () => {
            setVisible(window.scrollY > SCROLL_THRESHOLD);
        };

        /**
         * passive: true — указывает браузеру, что обработчик не вызовет
         * preventDefault(). Браузер может параллельно прокручивать страницу,
         * не дожидаясь завершения JS-кода. Улучшает плавность скролла.
         */
        window.addEventListener('scroll', handleScroll, { passive: true });

        // Проверяем сразу при монтировании — вдруг страница уже прокручена
        // (например, после навигации назад с сохранённой позицией скролла)
        handleScroll();

        // Cleanup: удаляем слушатель при размонтировании компонента
        return () => window.removeEventListener('scroll', handleScroll);
    }, []);

    // Не рендерим DOM-узел вообще когда кнопка не нужна.
    // Это лучше, чем visibility:hidden — не занимает место в tab-порядке.
    if (!visible) return null;

    return (
        <button
            onClick={() => window.scrollTo({ top: 0, behavior: 'smooth' })}
            aria-label="Вернуться наверх"
            className={[
                // Фиксированное позиционирование — независимо от прокрутки
                'fixed z-50 right-6',

                // Единое расстояние от низа на всех экранах —
                // FAB на /admin удалён, конкурирующих fixed-элементов нет
                'bottom-6',

                // Размер и форма — круглая кнопка
                'w-11 h-11 flex items-center justify-center rounded-full',

                // Типографика
                'font-bold text-lg text-white',

                // Тени и переходы
                'transition-all duration-200',

                // Интерактивные состояния: увеличение при hover, уменьшение при клике
                'hover:scale-110 active:scale-95',

                // Анимация появления (из globals.css): плавное возникновение
                'animate-fade-in',
            ].join(' ')}
            style={{
                background: 'var(--color-brand-600)',
                boxShadow: 'var(--shadow-hover)',
            }}
            onMouseEnter={(e) => {
                // Немного темнее при наведении (brand-700)
                (e.currentTarget as HTMLButtonElement).style.background =
                    'var(--color-brand-700)';
            }}
            onMouseLeave={(e) => {
                (e.currentTarget as HTMLButtonElement).style.background =
                    'var(--color-brand-600)';
            }}
        >
            ↑
        </button>
    );
}
